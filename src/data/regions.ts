// 전체 지역 수 계산
export const getTotalDistricts = () => {
    return jeonbukRegions.reduce((sum, region) => sum + region.districts.length, 0);
};

// 모든 URL 경로 생성 (SEO용)
export const getAllPaths = () => {
    const paths: { city: string; dong?: string }[] = [];

    for (const region of jeonbukRegions) {
        // 시/군 레벨 페이지
        paths.push({ city: region.id });

        // 동/읍/면 레벨 페이지
        for (const dong of region.districts) {
            paths.push({ city: region.id, dong });
        }
    }

    return paths;
};

